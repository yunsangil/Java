package ex1;

public class Two {
	public static void main(String[] args) {
		//수,우,미,양,가 프로그램
		int score=75; //미
		if(score>=90) {
			System.out.println("수");
		}
		else if(score>=80) {
			System.out.println("우");
			
		}
		else if(score>=70) {
			System.out.println("미");
			
		}
		else if(score>=60) {
			System.out.println("양");
			
		}
		else {
			System.out.println("가");
			
		}
	}

}
